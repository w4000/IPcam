#include "kwrap/type.h"
#include <kwrap/verinfo.h>

//=============================================================================
// AE version
//=============================================================================
VOS_MODULE_VERSION(nvt_ae, 1, 28, 010, 00);

//=============================================================================
// AE version
//=============================================================================
#define AE_VER_MAJOR       1
#define AE_VER_MINOR       0
#define AE_VER_MINOR2      1
#define AE_VER_MINOR3      0
#define AE_VER             ((AE_VER_MAJOR << 24) | (AE_VER_MINOR << 16) | (AE_VER_MINOR2 << 8) | AE_VER_MINOR3)

//=============================================================================
// extern functions
//=============================================================================
UINT32 ae_get_version(void)
{
	return AE_VER;
}

